% V - ???????? (??????? ?? ??? ?????)
V_V = V_star * (1 - beta * (1 - 2 .* xi));

% X - ?? ??? ??? (?????/?????)
V_X = V_star * (1 + delta * (1 - 4 .* xi .* (1 - xi) - 1/3));

% O - ????????? (?)
V_O = V_star * (1 + eta * (4 .* xi .* (1 - xi) - 2/3));

%% --- ????????? ????? ---
% U
P_U = phi0 * ones(size(xi));

% A - ?????????
P_A = phi0 * (1 + beta * (1 - 2 .* xi));

% V - ????????
P_V = phi0 * (1 - beta * (1 - 2 .* xi));

% X - ?? ??? ???
P_X = phi0 * (1 + delta * (1 - 4 .* xi .* (1 - xi) - 1/3));

% O - ?????????
P_O = phi0 * (1 + eta * (4 .* xi .* (1 - xi) - 2/3));

