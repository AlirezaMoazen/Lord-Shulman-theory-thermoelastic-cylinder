% ---------------- ???? ????? -----------------
function fracs = getDistribution(modelType, n)
    switch modelType
        case 'UD' % ???????
            fracs = ones(1,n);
        case 'A'  % ??????? ?? ????? ?????? ?? ??????
            fracs = linspace(0.2, 1, n);
        case 'V'  % ??????? ?? ??????? ?????? ?? ????
            fracs = linspace(1, 0.2, n);
        case 'X'  % ??????? ?? ?? ???? ?????? ???
            mid = floor(n/2);
            fracs = [linspace(1, 0.2, mid) linspace(0.2, 1, n-mid)];
        case 'O'  % ??????? ???? ?????? ??????
            mid = floor(n/2);
            fracs = [linspace(0.2, 1, mid) linspace(1, 0.2, n-mid)];
        otherwise
            fracs = ones(1,n);
    end
    % ?????????? ???? ??????? ?? ???? 0 ?? 1
    fracs = (fracs - min(fracs)) / (max(fracs)-min(fracs)+eps);
end
