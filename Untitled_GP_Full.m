%% ????? ???? ????
function [D, x] = chebD(N, a, b)
    if N==1, D=0; x=1; return, end
    x = cos(pi*(0:N-1)/(N-1))';
    c = [2; ones(N-2,1); 2].*(-1).^(0:N-1)';
    X = repmat(x,1,N); dX = X - X';
    D = (c*(1./c)')./(dX+eye(N));
    D = D - diag(sum(D,2));
    x = 0.5*((b-a)*x + (b+a));
end
