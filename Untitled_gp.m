    % Constructing the system
    A = diag(alpha(r)) * Dr * Dr + Dz * Dz;
    T_new = T + dt * (A * T); % Update rule (explicit scheme)
    % Apply boundary conditions
    T(:, end) = T(:, 1); % Example condition
end

