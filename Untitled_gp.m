function [D, W] = differential_weights(x)
    % Inputs:
    % x: Grid points (vector of size N)
    % Outputs:
    % D: Weight matrix for the first derivative
    % W: Weight matrix for the second derivative

    N = length(x);
    D = zeros(N, N); % Weights for the first derivative
    W = zeros(N, N); % Weights for the second derivative

    for i = 1:N
        for j = 1:N
            if i ~= j
                % First derivative weights
                D(i, j) = prod(x(i) - x([1:j-1, j+1:N])) / ...
                          prod(x(j) - x([1:j-1, j+1:N]));
                
                % Second derivative weights
                numerator = prod(x(i) - x([1:j-1, j+1:N]));
                denominator = (x(i) - x(j))^2 * prod(x(j) - x([1:j-1, j+1:N]));
                W(i, j) = numerator / denominator;
            end
        end
    end

    % Diagonal elements (self-contribution for second derivative)
    for i = 1:N
        W(i, i) = -sum(W(i, :));
    end
end


