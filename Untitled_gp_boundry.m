% function [D1_r, D2_r] = DQ_matrices(r)
%     N = length(r)
    D1_r = zeros(N, N);
    D2_r = zeros(N, N);
    for i = 1:N
        for j = 1:N
            if i ~= j
                prod = 1;
                for k = 1:N
                    if k ~= i && k ~= j
                        prod = prod * (r(i) - r(k)) / (r(j) - r(k));
                    end
                end
                D1_r(i,j) = prod / (r(i) - r(j));
            end
        end
        D1_r(i,i) = -sum(D1_r(i,:));
        for j = 1:N
            if i ~= j
                D2_r(i,j) = D1_r(i,j)*(D1_r(i,i) - D1_r(j,j))/(r(i) - r(j));
            end
        end
        D2_r(i,i) = -sum(D2_r(i,:));
    end
% end

% function [D1_z, D2_z] = DQ_matrices(z)
%     M = length(z)
    D1_z = zeros(M, M);
    D2_z = zeros(M, M);
    for i = 1:M
        for j = 1:M
            if i ~= j
                prod = 1;
                for k = 1:M
                    if k ~= i && k ~= j
                        prod = prod * (z(i) - z(k)) / (z(j) - z(k));
                    end
                end
                D1_z(i,j) = prod / (z(i) - z(j));
            end
        end
        D1_z(i,i) = -sum(D1_z(i,:));
        for j = 1:M
            if i ~= j
                D2_z(i,j) = D1_z(i,j)*(D1_z(i,i) - D1_z(j,j))/(z(i) - z(j));
            end
        end
        D2_z(i,i) = -sum(D2_z(i,:));
    end
% end



